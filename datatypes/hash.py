'''
Python program for initializing a dictionary or hash and print the second key value pair

Ouput
2->PHP
'''
#python doesnt need any declaration before usage of variable
b = {1:'Ruby',2:'PHP',3:'C',4:'C++',5:'Python'}
#printing the key and value using dictionary keys() and values() functions
print(str(b.keys()[1])+'->'+b.values()[1])
